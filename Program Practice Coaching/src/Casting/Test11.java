package Casting;

public class Test11 {
	void display1()
	{
		System.out.println("Display1");
	}
	void display2()
	{
		System.out.println("Display2");
		
	}
	void display3()
	{
		System.out.println("Display3");
	}
	public static void main(String[] args) {
		Test11 t11 = new Test11();
		t11.display1();
		t11.display2();
		t11.display3();
	}

}
